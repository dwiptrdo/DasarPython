# operator assignment juga bisa dipakai untuk operator bitwise
# contoh:
# OR (|)
c = False
print('\njika C = ',c)
c |= True
print('Maka Nilai C setelah di OR = ', c)

# AND (&)
c = False
print('\njika C = ',c)
c &= True
print('Maka Nilai C setelah di AND = ', c)

# XOR
c = False
print('\njika C = ',c)
c ^= True
print('Maka Nilai C setelah di XOR = ', c)
