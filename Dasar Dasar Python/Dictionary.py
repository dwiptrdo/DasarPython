# list --> array, mengakses dengan menggunakan index

data_list = ['Ali', 'Dwi', 'Aziz']

print(data_list[0])

# dictionary (dict) -> associative array mengakses dengan 
# identifier -> key

data_dict = {
    'key':'value',
    'al':'Ali',
    'dw':'Dwi',
    'az':'Aziz',
    'int': 100,
    'list': data_list
}

print(data_dict['az'])
print(data_dict['int'])
print(data_dict['list'])
